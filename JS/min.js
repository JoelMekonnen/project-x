    var notify = document.getElementById("notify");
    notify.onmouseover = function(){
    notify.style.borderStyle = "solid";
    notify.style.borderWidth = "0.5px";
    notify.style.padding = "4px";
}
    notify.onmouseout = function(){
    notify.style.borderStyle = "none";
    notify.style.padding = "0px";
}
